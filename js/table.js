function getMean(arr1, arr2) {
    let array = [];
    for (let i = 0; i<arr1.length; i++)
    {
        let item = (parseFloat(arr1[i]) + parseFloat(arr2[i])) / 2;
        array.push(item);
    }
    return array;
}

//Преобразует массив поправок в ассоциативный массив
function reformatArray(range_array) {
    let arr = {};
    for (let i in indexes)
    {
        arr[i] = range_array[indexes[i]];
    }
    return arr;
}

//Возвращает min и max дальность для снаряда
function getMinMaxRange(ranges) {

    if (!ranges.length)
        return false;

    let distance = [];
    for (let x in ranges){
        let dist = parseInt(ranges[x][indexes["distance"]]);
        distance.push(dist);
    }

    let range_min = Math.min.apply(null, distance);
    let range_max = Math.max.apply(null, distance);

    return [range_min, range_max];
}

//Возвращает таблицу поправок для данного орудия и снаряда
function getCannonTableArray(cannon_type, ammo_name) {
    let range_array = [];
    switch (cannon_type){
        case "2b14":
            range_array = get2b14ammoTables(ammo_name);
            break;
        case "d30":
            range_array = getD30Tables(ammo_name);
            break;
        case "2c3":
            range_array = get2C3Tables(ammo_name);
            break;
        case "bm21":
            range_array = getBM21Tables(ammo_name);
            break;
        default:
            return false;
    }
    return range_array;
}

//Находит ближайший массив поправок.
function findNearestRangeValue(cannon_type, ammo_name, range) {
    //Получаем таблицу поправок для данного орудия и снаряда
    let range_array = getCannonTableArray(cannon_type, ammo_name);

    if (range_array.length === 0)
        return false;

    let y0 = parseInt(range_array[0][indexes["distance"]]);
    for (let i = 1; i < range_array.length; i++)
    {
        let y1 = parseInt(range_array[i][indexes["distance"]]);

        if (range === y0)
            return range_array[i-1];

        if (range === y1)
            return range_array[i];

        if (y0 < range && range < y1) {
            return getMean(range_array[i - 1], range_array[i]);
        }

        y0 = y1;
    }

    return false;
}

